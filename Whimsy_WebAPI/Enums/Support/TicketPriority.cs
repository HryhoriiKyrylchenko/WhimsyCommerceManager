namespace Whimsy_WebAPI.Enums.Support
{
    /// <summary>
    /// Represents the priority levels for support tickets.
    /// </summary>
    public enum TicketPriority
    {
        /// <summary>
        /// Low priority indicates that the ticket can be addressed at a later time.
        /// </summary>
        Low,

        /// <summary>
        /// Medium priority indicates that the ticket should be addressed in a timely manner.
        /// </summary>
        Medium,

        /// <summary>
        /// High priority indicates that the ticket needs immediate attention and resolution.
        /// </summary>
        High
    }
}
