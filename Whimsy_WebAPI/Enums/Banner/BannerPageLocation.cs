namespace Whimsy_WebAPI.Enums.Banner
{
    /// <summary>
    /// Specifies the page location where a banner is displayed within the platform.
    /// </summary>
    public enum BannerPageLocation
    {
        /// <summary>
        /// The banner is displayed on the homepage.
        /// </summary>
        Homepage,

        /// <summary>
        /// The banner is displayed on a category page.
        /// </summary>
        CategoryPage,

        /// <summary>
        /// The banner is displayed on a product page.
        /// </summary>
        ProductPage,

        /// <summary>
        /// The banner is displayed on a seller's profile page.
        /// </summary>
        SellerProfile
    }
}
